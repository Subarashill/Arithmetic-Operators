#include <iostream>
using namespace std;

int main()
{

	short A = 10, B = 20;

	A += B; // is equivalent to A= A + B

	cout << "A = " << A << endl;

	A -= B; // is equivalent to A= A - B

	cout << "A = " << A << endl;

	A *= B; // is equivalent to A= A * B

	cout << "A = " << A << endl;

	A /= B; // is equivalent to A= A / B

	cout << "A = " << A << endl;

	A %= B; // is equivalent to A= A % B

	cout << "A = " << A << endl;





	return 0;
}